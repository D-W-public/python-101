# Demonstrate how to:
# -------------------
x = 5
y = 5.5
# 1) Convert an int to a float
# x = float(x)
# 2) Convert a float to an int
# y = int(y)
# 3) Perform division using a float and an int.
z = x / y
# 4) Use two variables to perform a multiplication.
zz = x * y
# What information is lost during which conversions?
# Nothing is lost form int to float. Everything behind the decimal point is lost the other way round.

print(y, x)

print(z, zz)
